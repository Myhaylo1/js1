let fact=n=>{
  if (typeof n!=='number') throw new TypeError('n is not a number');
  if (!Number.isSafeInteger(n)) throw new RangeError('n is not an integer');
  if (n<0) throw new RangeError('n is out of range');
  if (!n) return 1;
  return fact=n*fact(n-1);
}
